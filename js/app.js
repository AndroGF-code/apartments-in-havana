// Initialize
document.addEventListener('DOMContentLoaded', function() {
  // Language buttons
  const langButtons = document.querySelectorAll('.header__lang-btn');
  langButtons.forEach(btn => {
    btn.addEventListener('click', function() {
      const lang = this.getAttribute('data-lang');
      setLanguage(lang);
    });
  });

  // Book now buttons - pre-select apartment in contact form
  const bookButtons = document.querySelectorAll('.apartment__cta');
  bookButtons.forEach(btn => {
    btn.addEventListener('click', function(e) {
      const apartment = this.getAttribute('data-apartment');
      if (apartment) {
        const apartmentSelect = document.querySelector('select[name="apartment"]');
        if (apartmentSelect) {
          apartmentSelect.value = apartment;
        }
      }
    });
  });

  // Smooth scroll
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function(e) {
      e.preventDefault();
      const target = document.querySelector(this.getAttribute('href'));
      if (target) {
        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    });
  });

  // Form submission
  const contactForm = document.getElementById('contactForm');
  if (contactForm) {
    contactForm.addEventListener('submit', function(e) {
      e.preventDefault();
      handleFormSubmit(this);
    });
  }

  // Animate elements on scroll
  setupScrollAnimations();
});

// Set language
function setLanguage(lang) {
  currentLang = lang;
  
  // Update active button
  document.querySelectorAll('.header__lang-btn').forEach(btn => {
    btn.classList.remove('header__lang-btn--active');
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('header__lang-btn--active');
    }
  });

  // Update HTML lang attribute
  document.documentElement.lang = lang;

  // Update translatable elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // Store preference
  localStorage.setItem('preferredLang', lang);
}

